import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { variant?: "default" | "mayan" | "tropical" }
>(({ className, variant = "default", ...props }, ref) => {
  const variantStyles = {
    default: "rounded-lg border bg-card text-card-foreground shadow-sm",
    mayan: "rounded-lg border-2 border-accent/30 bg-card text-card-foreground shadow-md relative overflow-hidden before:absolute before:inset-0 before:border-b-4 before:border-accent",
    tropical: "rounded-xl border-0 bg-card text-card-foreground shadow-lg relative overflow-hidden after:absolute after:bottom-0 after:left-0 after:w-full after:h-1 after:bg-gradient-to-r after:from-primary after:via-accent after:to-secondary"
  }
  
  return (
    <div
      ref={ref}
      className={cn(
        variantStyles[variant],
        className
      )}
      {...props}
    />
  )
})
Card.displayName = "Card"

